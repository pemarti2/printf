#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				response;
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	if (n == 0)
		return (0);
	str1 = (unsigned char *) s1;
	str2 = (unsigned char *) s2;
	response = 0;
	i = 0;
	while (i < n - 1 && str1[i] == str2[i])
		i++;
	response = str1[i] - str2[i];
	return (response);
}
